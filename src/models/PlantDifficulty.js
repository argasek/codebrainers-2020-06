class PlantDifficulty {
  /**
   * @constructor
   * @param {number} id
   * @param {string} name
   * @param {number} value
   * @param {string} description
   */
  constructor(id, name, value, description) {
    this.id = id;
    this.name = name;
    this.value = value;
    this.description = description;
  }
}

export default PlantDifficulty;